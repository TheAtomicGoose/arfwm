    <strong>Hechos acerca del financiamiento del aborto</strong><br>
    <p>
        Con la decisión de la Corte Suprema en 1973 en el caso de Roe v. Wade, el aborto se hizo legal para todas las mujeres en Estados Unidos. Pero con la Enmienda Hyde de 1976, el Congreso corto todo financiamiento federal para abortos, y casi todos los estados lo siguieron. Los estados que reusan abortos pagados por Medicaid crearon una ley que les da el "derecho para elegir" a los que tienen dinero, y no a los demás. En estos estados, aproximadamente una en tres mujeres pobres son forzadas a continuar sus embarazos no deseados por que no tienen dinero. En este caso, frecuentemente abandonan sus sueños, como completar su educación, escapar de una situación violenta, o salir de la pobreza.
    </p>
    <br>
    <p>
        En Massachusetts, Medicaid si paga por el aborto, pero puede haber problemas inscribiendo a las mujeres rápidamente. Medicaid no cubre a los trabajadores pobres que no pueden pagar un seguro medico, ni a la gente que no tiene donde vivir. Mujeres en Massachusetts con un seguro medico federal – soldados y sus familias, Nativos Americanos usando el Indian Health Services, mujeres en prisiones federales, y otros empleados del gobierno federal – y mujeres que trabajan para algunas organizaciones religiosas, no pueden usar su seguro medico para pagar por un aborto. Aun cuando su seguro medico cubre el aborto, las mujeres en relaciones abusivas o situaciones peligrosas en su familia no podrán usarlo de manera segura. Estas son las mujeres que ayudamos.
    </p>
